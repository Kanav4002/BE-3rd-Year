// What is Node.js?
// Node.js is a runtime environment that allows you to run JavaScript code outside of a browser.
// It is built on top of the V8 JavaScript engine, which is the same engine that powers Chrome.
// It is a single-threaded, event-driven, non-blocking I/O model.
// It is designed to build scalable network applications.
// - runtime environment for JS.

// What is NPM?
// NPM is a package manager for Node.js.
// It is the default package manager for Node.js.
// It is a command-line tool that allows you to install, manage, and share packages.

// npm init 
// to initialize a new Node.js project.

// npm init -y
// skip extra details & fills required automatically.

// What is a package?
// A package is a directory containing a package.json file.
// The package.json file contains metadata about the package.
// The metadata includes the package name, version, description, and dependencies.

// package.json
// stores all the details of the application like scripts (short commands that can be used), dependencies, etc.

// dependencies
// stores external packages names and their versions we are using in application.
// so that we can use this to run application in future.

// What are node modules?
// stores all the packages which we install.

// what is port?
// port is a number that identifies a specific service on a computer.
// it is used to connect to a specific service on a computer.
